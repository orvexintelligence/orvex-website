import { createContext, useContext, useState } from 'react'

const LanguageContext = createContext(null)

function buildT(lang) {
  return function t(key) {
    try {
      if (!key || typeof key !== 'string') return ''
      const keys = key.split('.')
      let val = TRANSLATIONS[lang]
      for (const k of keys) {
        if (val == null || typeof val !== 'object') { val = undefined; break }
        val = val[k]
      }
      if (val !== undefined && val !== null && typeof val !== 'object') return String(val)
      val = TRANSLATIONS['IT']
      for (const k of keys) {
        if (val == null || typeof val !== 'object') { val = undefined; break }
        val = val[k]
      }
      if (val !== undefined && val !== null && typeof val !== 'object') return String(val)
      return key
    } catch { return key }
  }
}

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => {
    try { return localStorage.getItem('orvex_lang') || 'IT' } catch { return 'IT' }
  })
  const switchLang = (l) => {
    try { localStorage.setItem('orvex_lang', l) } catch {}
    setLang(l)
  }
  const t = buildT(lang)
  return (
    <LanguageContext.Provider value={{ lang, setLang: switchLang, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const ctx = useContext(LanguageContext)
  if (!ctx) return { lang: 'IT', setLang: () => {}, t: buildT('IT') }
  return ctx
}

